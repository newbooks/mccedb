<h2>Protein bioassembly based theoretical pKa database</h2>

<h3>What is this site?</h3>

<p> This site hosts theoretical pKas of protein residues calculated by MCCE method. For information about
    MCCE method, refer to <br>
    <i><a href="http://www3.interscience.wiley.com/cgi-bin/fulltext/122247007/HTMLSTART">Journal of
            Computational Chemistry Volume 30, Issue 14, Pages 2231-2247</a></i></p>

<h3>How are calculations done?</h3>

<p>This database is updated daily when new calculations are finished and refreshed. It is intended to cover
    all known structures available in PDB.</p>

<p>Calculations are based on bioassembly units as provided by PDB.</p>

